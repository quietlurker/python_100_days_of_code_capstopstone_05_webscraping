# get offers from zillow
offers = ZillowOffers(zillow_url)
offers.get_listings()

# create answers to google form - tuple of (addresses, prices, links)
listings = [(offers.addresses[i], offers.prices[i], offers.links[i]) for i in range(len(offers.addresses))]

# send all answers to google form
form = GoogleForm()
form.fill_form(listings)

